makeCacheMatrix <- function(x = matrix()) {
                m <- NULL ## initializing matrix
                set <- function(y){
                        x <<- y
                        m <<- NULL
                }
                get <- function() ##define the return matrix
                        x ##return special matrix
                setInv <- function(inverse) m <<- inverse ##assigning inverse matrix 
                getInv <- function() ##define function for returning the inverse matrix
                        m
                list(set = set, ##all defined functions in list 
                     get = get,
                     setInv = setInv,
                     getInv = getInv)
}


