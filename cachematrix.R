## Return a matrix that is the inverse of 'x'
cacheSolve <- function(x, ...) {
        m <- x$getInv() ##get inverse of the original matrix generated by makeCacheMatrix
        if (!is.null(m)){ ##Verify if the inverse has already been computed
                message("getting cached data")
                return(m)
        }
        data <- x$get()
        m <- solve(data, ...)
        x$setInv(m)
        m
}
